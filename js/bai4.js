/**
 ** Đầu vào
  - Chiều dài
  - Chiều rộng
 ** Xử lý
  - Tạo biến
   + dai: Cho nhập chiều dài
   + rong: Cho nhập chiều rộng
   + dienTich = dai * rong
   + chuVi = (dai + rong) / 2
 ** Đầu ra: Thông báo kết quả
 */

var dai = 6;
var rong = 2;
var dienTich = dai * rong;
var chuVi = (dai + rong) / 2;
console.log("Diện tích hình chữ nhật là: " + dienTich);
console.log("Chu vi hình chữ nhật là: " + chuVi);
